int
FindMarker(FILE *infile, const char *marker)
{
 char line[50];
 int i, len;
 int curr_line;

 len = (int)strlen(marker);
 if ( len > 50 -1 )
  len = 50 -1;

 for ( i=0; i<len; i++ )
   {
     curr_line = fgetc(infile);
     line[i] = curr_line;
     if (curr_line == (-1) )
       return 0;
   }
 line[len] = '\0';

 while ( 1 ) {
  if ( !strncmp(line, marker, len) )
   return 1;

  for ( i=0; i<len-1; i++ )
   line[i] = line[i+1];
  curr_line = fgetc(infile);
  line[len-1] = curr_line;
  if (curr_line == (-1) )
      return 0;
  line[len] = '\0';
 }


 return 0;
}




int
ReadHex(FILE *infile, unsigned char *A, int Length, char *str)
{
 int i, ch, started;
 unsigned char ich;

 if ( Length == 0 ) {
  A[0] = 0x00;
  return 1;
 }
 memset(A, 0x00, Length);
 started = 0;
 if ( FindMarker(infile, str) )
  while ( (ch = fgetc(infile)) != (-1) ) {
   if ( !isxdigit2(ch) ) {
    if ( !started ) {
     if ( ch == '\n' )
      break;
     else
      continue;
    }
    else
     break;
   }
   started = 1;
   if ( (ch >= '0') && (ch <= '9') )
    ich = ch - '0';
   else if ( (ch >= 'A') && (ch <= 'F') )
    ich = ch - 'A' + 10;
   else if ( (ch >= 'a') && (ch <= 'f') )
    ich = ch - 'a' + 10;
            else
                ich = 0;

   for ( i=0; i<Length-1; i++ )
    A[i] = (A[i] << 4) | (A[i+1] >> 4);
   A[Length-1] = (A[Length-1] << 4) | ich;
  }
 else
  return 0;

 return 1;
}

